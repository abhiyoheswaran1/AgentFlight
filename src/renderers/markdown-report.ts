import {
  compactCommandInText,
  formatCommandForDisplay,
  formatProofStatusForDisplay,
  formatReviewContractProofReferencesForDisplay,
  formatReviewContractReviewPathForDisplay,
  formatReviewContractStatusForDisplay,
  formatToolForReport,
  formatVerificationCountLine,
  formatVerificationFailureContext,
  formatVerifyCommandForDisplay
} from "../core/output.js";
import type { VerificationFailureCounts } from "../core/output.js";
import type {
  ReviewIntelligence,
  RiskAnalysis,
  SessionEvent,
  ToolAdapterResult,
  VerificationRun
} from "../types/index.js";

export interface MarkdownReportInput {
  task: string;
  sessionId: string;
  startedAt: string;
  changedFiles: string[];
  timelineEvents?: SessionEvent[] | undefined;
  risk: RiskAnalysis;
  verificationCommands: string[];
  verificationEvidence: VerificationRun[];
  verificationSummary?: VerificationFailureCounts | undefined;
  verificationGaps?: string[] | undefined;
  recommendation?: string | undefined;
  nextAction?: string | undefined;
  review?: ReviewIntelligence | undefined;
  tooling: {
    projscan: ToolAdapterResult;
    agentloopkit: ToolAdapterResult;
  };
}

export type MarkdownReportMode = "full" | "compact" | "pr-comment";

export interface MarkdownReportOptions {
  mode?: MarkdownReportMode | undefined;
}

export function renderMarkdownReport(
  input: MarkdownReportInput,
  options: MarkdownReportOptions = {}
): string {
  if (options.mode === "compact") return renderCompactMarkdownReport(input);
  if (options.mode === "pr-comment") return renderPrCommentDraft(input);

  return `# AgentFlight Proof Report

## Task
${input.task}

## Session
- Session ID: ${input.sessionId}
- Started: ${input.startedAt}

## Changed Files
${renderList(input.changedFiles, "No changed files detected.")}

## Risk Summary
- Level: ${input.risk.level}
- Changed files: ${input.risk.changedFiles}
${renderList(input.risk.reasons)}

## Verification Evidence
${renderVerification(input)}

## Review First
${renderReviewFirst(input)}

## Review Contract
${renderReviewContract(input)}

## Proof Gaps
${renderProofGaps(input)}

## Review Readiness
${renderReviewReadiness(input)}

## Recommendation
${renderRecommendation(input)}

## Next Action
${renderNextAction(input)}

## Timeline
${renderTimeline(input.timelineEvents ?? [])}

## Tooling
- ProjScan: ${formatToolForReport("ProjScan", input.tooling.projscan)}
- AgentLoopKit: ${formatToolForReport("AgentLoopKit", input.tooling.agentloopkit)}

Generated by AgentFlight, powered by ProjScan and AgentLoopKit where available.
`;
}

function renderPrCommentDraft(input: MarkdownReportInput): string {
  return `## AgentFlight Review Summary

Generated locally by AgentFlight. Not posted automatically.

- Task: ${input.task}
- Readiness: ${input.review?.readiness.label ?? input.recommendation ?? "Unknown"}
- Risk: ${input.risk.level}
- Changed files: ${input.changedFiles.length}

### Review first
${renderReviewFirst(input)}

### Proof gaps:
${renderPrCommentProofGaps(input)}

### Verification evidence
${renderVerification(input)}

### Next action
${renderNextAction(input)}
`;
}

function renderCompactMarkdownReport(input: MarkdownReportInput): string {
  return `# AgentFlight Compact Report

## Summary
- Task: ${input.task}
- Session ID: ${input.sessionId}
- Started: ${input.startedAt}
- Changed files: ${input.changedFiles.length}
- Risk: ${input.risk.level}

## Review Readiness
${renderReviewReadiness(input)}

## Review First
${renderReviewFirst(input)}

## Review Contract
${renderReviewContract(input)}

## Proof Gaps
${renderProofGaps(input)}

## Verification Evidence
${renderVerification(input)}

## Recommendation
${renderRecommendation(input)}

## Next Action
${renderNextAction(input)}

Generated by AgentFlight.
`;
}

function renderList(items: string[], empty = "None."): string {
  return items.length ? items.map((item) => `- ${item}`).join("\n") : empty;
}

function renderVerification(input: MarkdownReportInput): string {
  if (input.verificationEvidence.length === 0) {
    const commands = input.verificationCommands.length
      ? `\n\nSuggested commands:\n${renderList(input.verificationCommands)}`
      : "";
    const gaps =
      !input.review && input.verificationGaps?.length
        ? `\n\nGaps:\n${renderList(input.verificationGaps)}`
        : "";
    return `No verification evidence recorded.${gaps}${commands}`;
  }

  const evidence = input.verificationEvidence
    .map((run) => {
      const command = formatCommandForDisplay(run.command);
      const base = `- ${command}: ${run.status} (exit ${run.exitCode ?? "unknown"}, ${run.durationMs}ms)\n  - stdout: ${run.stdoutPath}\n  - stderr: ${run.stderrPath}`;
      if (run.status === "failed" && run.outputExcerpt) {
        return `${base}\n  - Output excerpt:\n\n${renderExcerptFence(run.outputExcerpt)}`;
      }
      return base;
    })
    .join("\n");
  const summary = input.verificationSummary ?? summarizeVerificationEvidence(input);
  const failureContext = formatVerificationFailureContext(summary);
  const summaryText = `${formatVerificationCountLine(summary)}${failureContext ? `\n${failureContext}` : ""}`;
  const gaps =
    !input.review && input.verificationGaps?.length
      ? `\n\nGaps:\n${renderList(input.verificationGaps)}`
      : "";

  return `${summaryText}\n${evidence}${gaps}`;
}

function summarizeVerificationEvidence(input: MarkdownReportInput): VerificationFailureCounts {
  const passed = input.verificationEvidence.filter((run) => run.status === "passed").length;
  const failed = input.verificationEvidence.filter((run) => run.status === "failed").length;
  return {
    passed,
    failed,
    unresolvedFailed: failed,
    resolvedFailed: 0
  };
}

function renderExcerptFence(excerpt: string): string {
  // Use a longer fence if the excerpt itself contains a triple backtick.
  const fence = excerpt.includes("```") ? "````" : "```";
  return `${fence}text\n${excerpt}\n${fence}`;
}

function renderTimeline(events: SessionEvent[]): string {
  if (events.length === 0) return "No timeline events recorded.";
  return events
    .map((event) => {
      const message = event.message ? ` - ${event.message}` : "";
      return `- ${event.timestamp}: ${event.title}${message}`;
    })
    .join("\n");
}

function renderReviewFirst(input: MarkdownReportInput): string {
  const focus = input.review?.focus;
  if (focus && focus.length > 0) {
    return focus
      .map(
        (item) =>
          `${item.rank}. ${item.file}\n   - Proof: ${formatProofStatusForDisplay(item.proofStatus)}\n   - Why: ${item.reasons.join("; ")}\n   - Focus: ${item.suggestedReviewerFocus}${item.suggestedCommand ? `\n   - Suggested proof: ${formatVerifyCommandForDisplay(item.suggestedCommand)}` : ""}`
      )
      .join("\n");
  }

  return input.risk.categories.length
    ? input.risk.categories
        .map((summary) => `- ${summary.category}: ${summary.files.join(", ")}`)
        .join("\n")
    : "- Confirm the session has meaningful changes before review.";
}

function renderProofGaps(input: MarkdownReportInput): string {
  if (input.review) {
    return input.review.proofGaps.length
      ? input.review.proofGaps
          .map(
            (gap) =>
              `- ${gap.severity}: ${compactCommandInText(gap.message, gap.suggestedCommand)}${gap.suggestedCommand ? `\n  - Suggested proof: ${formatVerifyCommandForDisplay(gap.suggestedCommand)}` : ""}`
          )
          .join("\n")
      : "- none";
  }

  return input.verificationGaps?.length ? renderList(input.verificationGaps) : "- none";
}

function renderReviewContract(input: MarkdownReportInput): string {
  const contract = input.review?.contract;
  if (!contract || contract.claims.length === 0) return "- No review contract claims recorded.";

  const reviewPath = formatReviewContractReviewPathForDisplay(contract);
  const claims = contract.claims
    .map((claim) => {
      const files = claim.files.length ? `\n  - Files: ${claim.files.join(", ")}` : "";
      const evidence = claim.evidence.length ? `\n  - Evidence: ${claim.evidence.join("; ")}` : "";
      const proofReferences = formatReviewContractProofReferencesForDisplay(claim);
      const proofReferenceLine = proofReferences ? `\n  - ${proofReferences}` : "";
      const command = claim.suggestedCommand
        ? `\n  - Suggested proof: ${formatVerifyCommandForDisplay(claim.suggestedCommand)}`
        : "";
      return `- ${formatReviewContractStatusForDisplay(claim.status)} - ${claim.text}${files}${evidence}${proofReferenceLine}${command}`;
    })
    .join("\n");
  return [reviewPath, claims].filter(Boolean).join("\n\n");
}

function renderPrCommentProofGaps(input: MarkdownReportInput): string {
  const gaps = input.review?.proofGaps ?? [];
  if (gaps.length === 0 && !input.verificationGaps?.length) return "Proof gaps: none";
  return renderProofGaps(input);
}

function renderReviewReadiness(input: MarkdownReportInput): string {
  if (!input.review) return input.recommendation ?? "Unknown";
  const command = input.review.readiness.suggestedCommand;
  return `${input.review.readiness.label}

Reason: ${compactCommandInText(input.review.readiness.reason, command)}
Next action: ${compactCommandInText(input.review.readiness.nextAction, command)}`;
}

function renderRecommendation(input: MarkdownReportInput): string {
  if (input.recommendation) {
    return `${input.recommendation}.`;
  }
  if (input.verificationEvidence.length === 0) {
    return "Do not claim completion yet. Run the suggested verification commands and regenerate the report.";
  }
  if (input.risk.level === "high") {
    return "Request careful human review focused on the high-risk file categories before merging.";
  }
  return "Review the changed files and verification evidence before handoff.";
}

function renderNextAction(input: MarkdownReportInput): string {
  if (input.nextAction) {
    return compactCommandInText(input.nextAction, input.review?.readiness.suggestedCommand);
  }
  if (input.verificationEvidence.length === 0 && input.verificationCommands.length > 0) {
    return `Run ${input.verificationCommands[0]} and capture the result.`;
  }
  return "Prepare a scoped handoff for the next agent or reviewer.";
}
