export interface MarkdownReportInput {
  task: string;
  sessionId: string;
  startedAt: string;
  changedFiles: string[];
  timelineEvents?: SessionEvent[] | undefined;
  risk: RiskAnalysis;
  verificationCommands: string[];
  verificationEvidence: VerificationRun[];
  verificationSummary?: VerificationFailureCounts | undefined;
  verificationGaps?: string[] | undefined;
  recommendation?: string | undefined;
  nextAction?: string | undefined;
  review?: ReviewIntelligence | undefined;
  baseframeResult?: AgentFlightResultV1 | undefined;
  tooling: {
    projscan: ToolAdapterResult;
    agentloopkit: ToolAdapterResult;
  };
}

export type MarkdownReportMode = "full" | "compact" | "pr-comment";

export interface MarkdownReportOptions {
  mode?: MarkdownReportMode | undefined;
}

export function renderMarkdownReport(
  input: MarkdownReportInput,
  options: MarkdownReportOptions = {}
): string {
  if (options.mode === "compact") return renderCompactMarkdownReport(input);
  if (options.mode === "pr-comment") return renderPrCommentDraft(input);

  return `# AgentFlight Proof Report

## Task
${escapeMarkdownTextForDisplay(input.task)}

## Session
- Session ID: ${escapeMarkdownTextForDisplay(input.sessionId)}
- Started: ${escapeMarkdownTextForDisplay(input.startedAt)}

## Changed Files
${renderChangedFileList(input.changedFiles)}

## Risk Summary
- Level: ${input.risk.level}
- Changed files: ${input.risk.changedFiles}
${renderList(input.risk.reasons)}

${renderBaseframeIntegration(input)}

## Verification Evidence
${renderVerification(input)}

## Review First
${renderReviewFirst(input)}

## Trust Delta
${renderTrustDelta(input)}

## Review Queue
${renderReviewQueue(input)}

## Review Routing
${renderReviewRouting(input)}

${renderFullSuggestedCommands(input)}

## Review Receipt
${renderReviewReceipt(input)}

## Required Proof
${renderRequiredProof(input)}

${renderProofFreshnessSection(input)}

## Repo Calibration
${renderRepoCalibration(input.review?.calibration)}

## Review Contract
${renderReviewContract(input)}

## Proof Gaps
${renderProofGaps(input)}

## Review Readiness
${renderReviewReadiness(input)}

## Recommendation
${renderRecommendation(input)}

## Next Action
${renderNextAction(input)}

## Timeline
${renderTimeline(input.timelineEvents ?? [])}

## Tooling
- ProjScan: ${md(formatToolForReport("ProjScan", input.tooling.projscan))}
- AgentLoopKit: ${md(formatToolForReport("AgentLoopKit", input.tooling.agentloopkit))}

Generated by AgentFlight, powered by ProjScan and AgentLoopKit where available.
`;
}

