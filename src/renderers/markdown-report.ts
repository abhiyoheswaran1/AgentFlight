function renderList(items: string[], empty = "None."): string {
  return items.length
    ? items.map((item) => `- ${escapeMarkdownTextForDisplay(item)}`).join("\n")
    : empty;
}

function renderBaseframeIntegration(input: MarkdownReportInput): string {
  return input.baseframeResult ? formatBaseframeResultForMarkdown(input.baseframeResult) : "";
}

function renderChangedFileList(files: string[]): string {
  if (files.length === 0) return "No changed files detected.";
  const visibleFiles = files.slice(0, 80);
  const rows = visibleFiles.map((file) => `- ${escapeMarkdownTextForDisplay(file)}`);
  const remaining = files.length - visibleFiles.length;
  if (remaining > 0) rows.push(`- ${remaining} more changed files in replay/status JSON.`);
  return rows.join("\n");
}

function renderVerification(input: MarkdownReportInput): string {
  if (input.verificationEvidence.length === 0) {
    const commands = input.verificationCommands.length
      ? `\n\nSuggested commands:\n${renderList(input.verificationCommands)}`
      : "";
    const gaps =
      !input.review && input.verificationGaps?.length
        ? `\n\nGaps:\n${renderList(input.verificationGaps)}`
        : "";
    return `No verification evidence recorded.${gaps}${commands}`;
  }

  const evidence = input.verificationEvidence
    .map((run) => {
      const command = markdownText(formatCommandForDisplay(run.command));
      const base = `- ${command}: ${md(run.status)} (exit ${run.exitCode ?? "unknown"}, ${run.durationMs}ms)\n  - stdout: ${md(run.stdoutPath)}\n  - stderr: ${md(run.stderrPath)}`;
      if (run.status === "failed" && run.outputExcerpt) {
        return `${base}\n  - Output excerpt:\n\n${renderExcerptFence(run.outputExcerpt)}`;
      }
      return base;
    })
    .join("\n");
  const summary = input.verificationSummary ?? summarizeVerificationEvidence(input);
  const failureContext = formatVerificationFailureContext(summary);
  const summaryText = `${formatVerificationCountLine(summary)}${failureContext ? `\n${failureContext}` : ""}`;
  const gaps =
    !input.review && input.verificationGaps?.length
      ? `\n\nGaps:\n${renderList(input.verificationGaps)}`
      : "";

  return `${summaryText}\n${evidence}${gaps}`;
}

function summarizeVerificationEvidence(input: MarkdownReportInput): VerificationFailureCounts {
  const passed = input.verificationEvidence.filter((run) => run.status === "passed").length;
  const failed = input.verificationEvidence.filter((run) => run.status === "failed").length;
  return {
    passed,
    failed,
    unresolvedFailed: failed,
    resolvedFailed: 0
  };
}

function renderExcerptFence(excerpt: string): string {
  const longestBacktickRun = Math.max(
    2,
    ...Array.from(excerpt.matchAll(/`+/g), (match) => match[0].length)
  );
  const fence = "`".repeat(longestBacktickRun + 1);
  return `${fence}text\n${excerpt}\n${fence}`;
}

function renderTimeline(events: SessionEvent[]): string {
  if (events.length === 0) return "No timeline events recorded.";
  return events
    .map((event) => {
      const message = event.message ? ` - ${md(event.message)}` : "";
      return `- ${md(event.timestamp)}: ${md(event.title)}${message}`;
    })
    .join("\n");
}

function renderReviewFirst(input: MarkdownReportInput): string {
  const focus = input.review?.focus;
  if (focus && focus.length > 0) {
    return focus
      .map(
        (item) =>
          `${item.rank}. ${escapeMarkdownTextForDisplay(item.file)}\n   - Proof: ${md(formatProofStatusForDisplay(item.proofStatus))}\n   - Why: ${escapeMarkdownTextForDisplay(item.reasons.join("; "))}\n   - Focus: ${escapeMarkdownTextForDisplay(item.suggestedReviewerFocus)}${item.suggestedCommand ? `\n   - Suggested proof: ${markdownText(formatVerifyCommandForDisplay(item.suggestedCommand))}` : ""}`
      )
      .join("\n");
  }

  return input.risk.categories.length
    ? input.risk.categories
        .map((summary) => `- ${md(summary.category)}: ${md(summary.files.join(", "))}`)
        .join("\n")
    : "- Confirm the session has meaningful changes before review.";
}

function renderRequiredProof(input: MarkdownReportInput): string {
  return renderProjectReviewContract(input.review?.projectReviewContract);
}

function renderProjectReviewContract(
  contract: ProjectReviewContractEvaluation | undefined
): string {
  if (!contract) return "- No project review contract configured.";
  if (!contract.enabled) return "- Project review contract disabled.";
  if (contract.requirements.length === 0) {
    return "- No project review contract requirements matched these changes.";
  }
  return contract.requirements.map(renderProjectRequirement).join("\n");
}

function renderProjectRequirement(requirement: ProjectReviewRequirementStatus): string {
  const details = formatProjectRequirementDetailsForDisplay(requirement)
    .map((line) => `\n  - ${md(line)}`)
    .join("");
  return `- ${md(formatProjectRequirementStatusForDisplay(requirement.status))} - ${md(requirement.label)}${details}`;
}

function renderRepoCalibration(calibration: ProofCalibration | undefined): string {
  if (!calibration) return "- No repo calibration history loaded.";
  if (calibration.suggestions.length === 0) {
    return `- ${md(formatProofCalibrationSummaryForDisplay(calibration))}`;
  }
  return [
    `- ${md(formatProofCalibrationSummaryForDisplay(calibration))}`,
    ...calibration.suggestions.map(renderRepoCalibrationSuggestion)
  ].join("\n");
}

function renderRepoCalibrationSuggestion(suggestion: ProofCalibrationSuggestion): string {
  const details = formatProofCalibrationDetailsForDisplay(suggestion)
    .map((line) => `\n  - ${md(line)}`)
    .join("");
  return `- ${md(formatProofCalibrationStatusForDisplay(suggestion.status))} - ${md(suggestion.category)}${details}`;
}

function renderProofFreshnessSection(input: MarkdownReportInput, heading = "##"): string {
  const lines = formatProofFreshnessAttributionForDisplay(input.review?.proofFreshness);
  if (lines.length === 0) return "";
  return `${heading} Proof Freshness\n${lines.map((line) => `- ${md(line)}`).join("\n")}`;
}

